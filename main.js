// Create a contract instance
let contract = new web3.eth.Contract(ABI, contractAddress);

async function claimTokens() {
    try {
        // Request account access
        let accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });

        // Check if the claimTokens function exists in the contract
        if (typeof contract.methods['claimTokens'] !== 'function') {
          throw new Error('The claimTokens function does not exist in the contract');
        }

        // Call the claimTokens function on your contract
        await contract.methods.claimTokens().send({from: accounts[0]});
        alert('Tokens claimed!');
    } catch (error) {
        console.error(error);
        alert('You have already claimed tokens today!');
    }
}

document.getElementById('claimButton').addEventListener('click', claimTokens);